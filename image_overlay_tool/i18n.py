def resolve_language(requested_language: str) -> str:
    if requested_language in LANGUAGE_NAMES:
        return requested_language

    locale_value = (
        os.environ.get("LC_ALL")
        or os.environ.get("LC_MESSAGES")
        or os.environ.get("LANG")
        or ""
    ).lower()
    return "zh" if locale_value.startswith("zh") else "en"


def get_text(language: str, key: str, **kwargs: object) -> str:
    template = TEXTS[language][key]
    return template.format(**kwargs)
